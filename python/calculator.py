def add(a,b):
    add_res = a + b
    return add_res

def substract(a,b):
    sub_rest = a - b
    return sub_rest
    
def multiply(a,b):
    multiply_res = a * b
    return multiply_res

def divide(a,b):
    divide_res = a / b
    return divide_res
    
def exp(a,b):
    exp_res = a ** b
    return exp_res

print(f"Add result: {add(2,2)}")
print(f"Substract result: {substract(2,2)}")
print(f"Multiply result: {multiply(2,2)}")
print(f"Divide result: {divide(2,2)}")
print(f"Exponent result: {exp(2,2)}")
